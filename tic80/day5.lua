function TIC()
	for y=-68,67 do
		for x=-120,119 do
			X=(math.atan2(y,x)+math.pi)*2.546
			Y=300//(x*x+y*y+1)^0.5
			t=time()//100
			c=(X//1)~(Y//1)+t
			pix(120+x,68+y,c)
		end
	end
end

